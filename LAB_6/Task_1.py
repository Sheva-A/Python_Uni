import random  # Імпортуємо модуль для генерації випадкових чисел

# Генеруємо випадкову кількість монет від 1 до 1000
coins = random.randint(1, 1000)
print(f"Ви знайшли {coins} золотих монет!")

# Пробуємо отримати кількість людей у команді та поділити монети
try:
    # Користувач вводить кількість людей у команді
    team_size = int(input("Введіть кількість людей у команді: "))

    # Ділимо монети на всіх членів команди
    coins_per_person = coins // team_size
    print(f"Кожен член команди отримує {coins_per_person} монет.")

# Якщо користувач ввів не ціле число
except ValueError:
    print("Помилка: введено некоректне значення. Будь ласка, введіть ціле число.")

# Якщо користувач ввів 0 (ділення на нуль неможливе)
except ZeroDivisionError:
    print("Помилка: на команду з 0 людей поділити неможливо!")

# Цей блок виконається завжди
finally:
    print("Пригоди тривають!")

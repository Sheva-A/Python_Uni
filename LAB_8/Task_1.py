# Отримуємо рядок від користувача
text = input("Введіть рядок: ")

# Визначаємо українські голосні літери (включаючи великі)
vowels_ukr = "аеєиіїоуюяАЕЄИІЇОУЮЯ"

# Визначаємо українські приголосні літери (включаючи великі)
consonants_ukr = "бвгґджзйклмнпрстфхцчшщБВГҐДЖЗЙКЛМНПРСТФХЦЧШЩ"

# Ініціалізуємо лічильники та список знайдених голосних
vowel_count = 0
consonant_count = 0
found_vowels = []

# Проходимо по кожному символу у введеному рядку
for char in text:
    # Якщо символ — голосна літера
    if char in vowels_ukr:
        vowel_count += 1  # Збільшуємо лічильник голосних
        found_vowels.append(char)  # Додаємо літеру до списку знайдених голосних
    # Якщо символ — приголосна літера
    elif char in consonants_ukr:
        consonant_count += 1  # Збільшуємо лічильник приголосних

# Виводимо кількість голосних літер
print("Кількість голосних літер:", vowel_count)

# Виводимо кількість приголосних літер
print("Кількість приголосних літер:", consonant_count)

# Виводимо список усіх знайдених голосних
print("Список голосних літер:", found_vowels)

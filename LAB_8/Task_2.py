# Два списки з числами
list1 = [4, 2, 7, 2, 9, 4]
list2 = [3, 7, 1, 4, 3]

# Перетворюємо списки на множини, щоб залишити лише унікальні значення
unique1 = set(list1)
unique2 = set(list2)

# Об'єднуємо обидві множини та сортуємо результат
combined = sorted(unique1.union(unique2))

# Перевіряємо, чи список не порожній, і виводимо результат
if combined:
    print("Об'єднаний та відсортований список:", combined)
else:
    print("Результуючий список порожній.")

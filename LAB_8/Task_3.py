# Початковий рядок для аналізу
text = "Замовнику варто було б навчитися розуміти свої бажання!"

# Створюємо порожній рядок для збереження очищеного тексту
cleaned = ""
for char in text:
    # Додаємо лише літери та цифри, перетворюючи їх на малі
    if char.isalnum():
        cleaned += char.lower()

# Створюємо словник для підрахунку кількості кожного символу
counts = {}
for char in cleaned:
    # Якщо символ уже є в словнику, збільшуємо лічильник
    if char in counts:
        counts[char] += 1
    else:
        # Якщо символ ще не зустрічався, додаємо його зі значенням 1
        counts[char] = 1

# Виводимо кількість унікальних символів (ключів у словнику)
print("Кількість унікальних символів:", len(counts))

# Виводимо символи, які зустрічаються більше одного разу
print("Повторювані символи:")
found = False
for char in counts:
    if counts[char] > 1:
        print(f"{char}: {counts[char]}")
        found = True

# Якщо немає повторів, повідомляємо про це
if not found:
    print("Немає символів, що повторюються.")

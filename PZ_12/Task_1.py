"""
Програма перевіряє наявність міст "Одеса" та "Полтава" у списку міст
з використанням множини для пришвидшення пошуку.
"""

cities_list = ["Київ", "Одеса", "Львів", "Харків", "Житомир"]
cities_set = set(cities_list)

for city in ["Одеса", "Полтава"]:
    if city in cities_set:
        print(f"Місто {city} є у списку.")
    else:
        print(f"Міста {city} немає у списку.")

"""
Програма дозволяє користувачу ввести ім’я студента і виводить його оцінку.
У разі відсутності імені в словнику, повідомляється про це.
"""

students = {"Іван": 80, "Марія": 95, "Олег": 78, "Анна": 85}

name = input("Введіть ім’я студента: ")

if name in students:
    print(f"Оцінка студента {name}: {students[name]}")
else:
    print("Такого студента немає в списку.")

number = int(input("Введіть трицифрове число: ")) #Користувач вводить число

#Виділяємо та порівнюємо першу та останню цифри
#Якщо цифри рівні, виводимо "Число є паліндромом"
#Інакше виводимо "Число не є паліндромом"
if  number//100 == number%10:
    print(f"Число {number} є паліндромом")
else:
    print(f"Число {number} не є паліндромом")